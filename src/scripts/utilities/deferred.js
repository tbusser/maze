/* ========================================================================== *\
	PRIVATE VARIABLES
\* ========================================================================== */

const
	propertyNames = {
		promise: Symbol('promise'),
		reject: Symbol('reject'),
		resolve: Symbol('resolve')
	};

/* == PRIVATE VARIABLES ===================================================== */



/* ========================================================================== *\
	PRIVATE METHODS
\* ========================================================================== */

/**
 *
 * @private
 * @memberof Deferred
 */
function initPromise() {
	this[propertyNames.promise] = new Promise((resolve, reject) => {
		this[propertyNames.reject] = reject;
		this[propertyNames.resolve] = resolve;
	});
}

/* == PRIVATE METHODS ======================================================= */



/* ========================================================================== *\
	PUBLIC API
\* ========================================================================== */

class Deferred {
	/* ====================================================================== *\
		CONSTRUCTOR
	\* ====================================================================== */
	constructor() {
		initPromise.call(this);
	}
	/* == CONSTRUCTOR ======================================================= */



	/* ====================================================================== *\
		INSTANCE PROPERTIES
	\* ====================================================================== */

	/* ---------------------------------- *\
		promise (read-only)
	\* ---------------------------------- */
	get promise() {
		return this[propertyNames.promise];
	}
	/* -- promise (read-only) ----------- */


	/* ---------------------------------- *\
		reject (read-only)
	\* ---------------------------------- */
	get reject() {
		return this[propertyNames.reject];
	}
	/* -- reject (read-only) ------------ */


	/* ---------------------------------- *\
		resolve (read-only)
	\* ---------------------------------- */
	get resolve() {
		return this[propertyNames.resolve];
	}
	/* -- resolve (read-only) ----------- */

	/* == INSTANCE PROPERTIES =============================================== */
}

/* == PUBLIC API ============================================================ */



/* ========================================================================== *\
	EXPORTS
\* ========================================================================== */

export default Deferred;

/* == EXPORTS =============================================================== */
