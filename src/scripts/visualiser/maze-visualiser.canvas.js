/* ========================================================================== *\
	IMPORTS
\* ========================================================================== */

import VisualiserBase from './maze-visualiser.base.js';

/* == IMPORTS =============================================================== */



/* ========================================================================== *\
	PRIVATE VARIABLES
\* ========================================================================== */

const
	selectors = {
		canvas: 'js-output'
	};

/* == PRIVATE VARIABLES ===================================================== */



/* ========================================================================== *\
	PUBLIC API
\* ========================================================================== */

class MazeVisualiserCanvas extends VisualiserBase {
	/* ====================================================================== *\
		CONSTRUCTOR
	\* ====================================================================== */
	constructor(baseElement) {
		super(baseElement);
	}
	/* == CONSTRUCTOR ======================================================= */



	/* ====================================================================== *\
		OVERRIDDEN METHODS
	\* ====================================================================== */

	__initVisualisation(rows, columns) {
		/** @type {HTMLCanvasElement} */
		const
			canvas = this.baseElement.querySelector(selectors.canvas);
		if (canvas === null) {
			return;
		}

		canvas.style.width = `${(columns * this.cellSize)}px`;

		/** @type {CanvasRenderingContext2D} */
		const
			context = canvas.getContext();
		context.clearRect(0, 0, canvas.width, canvas.height);
	}

	/* == OVERRIDDEN METHODS ================================================ */
}

/* == PUBLIC API ============================================================ */



/* ========================================================================== *\
	EXPORTS
\* ========================================================================== */

export default MazeVisualiserCanvas;

/* == EXPORTS =============================================================== */
